"""
core/plugin_scaffolder.py
--------------------------
Deterministic (zero-LLM) plugin scaffolding.

`scaffold_plugin(spec, ...)` generates a plugins/<target_id>/ directory with the
four files every plugin needs (__init__.py, parser.py, rules.py,
build_<target_id>.py), following the same structure as the hand-written SSH and
Nginx config_assessment.plugins. The generated code imports and runs as-is; the user then edits it
to refine detection or scoring.

Templates are plain f-strings (no Jinja2 — four simple files). Literal braces in
the emitted Python are escaped as {{ }}.
"""

from __future__ import annotations

import shutil
from dataclasses import dataclass, field
from pathlib import Path
from typing import Optional


@dataclass
class PluginSpec:
    service_name: str            # "PostgreSQL"
    target_id: str               # "postgresql"
    config_format: str           # "key_value" | "ini" | "json"
    config_paths: list[str]      # ["/etc/postgresql/*/main/postgresql.conf"]
    config_filenames: list[str]  # ["postgresql.conf"]
    bind_directive: Optional[str]  # "listen_addresses" — to infer AV (or None)
    version_exposing: list[str]  # [] for PostgreSQL
    entries: list = field(default_factory=list)         # MisconfigEntry-like
    absence_rules: list = field(default_factory=list)    # absence rule-like
    benchmark_source: str = ""   # "CIS PostgreSQL Benchmark v1.0.0"
    version: str = "1.0"


def _class_name(service_name: str) -> str:
    """"PostgreSQL" -> "Postgresql"; "Test Service" -> "TestService"."""
    cleaned = "".join(c if c.isalnum() else " " for c in service_name)
    return "".join(w.capitalize() for w in cleaned.split()) or "Generated"


# Map config_format → the generic parser module to delegate to.
_PARSER_MODULE = {
    "key_value": "config_assessment.parsers.key_value",
}


# ------------------------------------------------------------------ #
# Templates                                                            #
# ------------------------------------------------------------------ #

def _render_parser(spec: PluginSpec) -> str:
    parser_mod = _PARSER_MODULE.get(spec.config_format, "config_assessment.parsers.key_value")
    return f'''"""
plugins/{spec.target_id}/parser.py — GENERATED by `ccss plugin add`.

Thin wrapper over the generic {spec.config_format} parser. Edit only if this
target needs format-specific handling.
"""

from {parser_mod} import parse_file as parse_file  # re-export
'''


def _render_rules(spec: PluginSpec) -> str:
    bind = spec.bind_directive
    if bind:
        av_body = f'''    bind_dirs = [d for d in directives if d.name == "{bind}"]
    if not bind_dirs:
        return "N", "No {bind}; service defaults to network exposure"
    values = [d.value.strip() for d in bind_dirs]
    loopback = ("127.", "::1", "localhost", "[::1]")
    all_loopback = all(
        any(v.lower().strip("[]").startswith(p) or v.lower().strip("[]") == p for p in loopback)
        for v in values
    )
    if all_loopback:
        return "L", f"All {bind} entries bound to loopback only: {{values}}"
    return "N", f"At least one {bind} exposes a non-loopback address: {{values}}"'''
    else:
        av_body = '    return "N", "Network service (no bind directive declared)"'

    return f'''"""
plugins/{spec.target_id}/rules.py — GENERATED by `ccss plugin add`.

Deterministic SystemProfile inference. Edit the AV/Au logic for this service.
"""

from __future__ import annotations

from config_assessment.core.models import AuValue, AVValue, Directive, SystemProfile


def _infer_av(directives: list[Directive]) -> tuple[AVValue, str]:
{av_body}


def _infer_au(directives: list[Directive]) -> tuple[AuValue, str]:
    # Default: Au=None (safe default — most service misconfigs are exploitable
    # without valid credentials). Edit if this service authenticates first.
    return "N", "Au=None (default; edit for {spec.service_name})"


def infer_profile(directives: list[Directive]) -> SystemProfile:
    av, rationale_av = _infer_av(directives)
    au, rationale_au = _infer_au(directives)
    return SystemProfile(av=av, au=au, rationale_av=rationale_av, rationale_au=rationale_au)
'''


def _render_init(spec: PluginSpec) -> str:
    cls = _class_name(spec.service_name)
    filenames_repr = repr(set(spec.config_filenames))
    exposing_repr = repr(tuple(spec.version_exposing))
    return f'''"""
plugins/{spec.target_id}/__init__.py — GENERATED by `ccss plugin add`.

Edit this file to customise detection and metadata for {spec.service_name}.
"""

from __future__ import annotations

from pathlib import Path

from config_assessment.core.models import Directive, SystemProfile, TargetMetadata
from config_assessment.core.runtime import register_plugin
from config_assessment.core.target import (
    Target,
    CONFIDENCE_EXACT_FILENAME,
    CONFIDENCE_SYNTAX_MARKER,
    CONFIDENCE_DIRECTORY,
)
from config_assessment.plugins.{spec.target_id}.parser import parse_file
from config_assessment.plugins.{spec.target_id}.rules import infer_profile

_FILENAMES = {filenames_repr}


class {cls}Plugin(Target):
    """{spec.service_name} target plugin ({spec.benchmark_source})."""

    def detect(self, path: str) -> bool:
        p = Path(path)
        if p.is_file():
            return p.name in _FILENAMES
        if p.is_dir():
            return any((p / fn).exists() for fn in _FILENAMES)
        return False

    def parse_config(self, path: str) -> list[Directive]:
        p = Path(path)
        if p.is_dir():
            for fn in _FILENAMES:
                entry = p / fn
                if entry.exists():
                    return parse_file(str(entry))
            return []
        return parse_file(path)

    def get_profile(self, directives: list[Directive]) -> SystemProfile:
        return infer_profile(directives)

    def metadata(self) -> TargetMetadata:
        return TargetMetadata(
            name="{spec.target_id}",
            display_name="{spec.service_name}",
            version="{spec.version}",
            benchmark_source="{spec.benchmark_source}",
            priority=100,
            version_exposing_directives={exposing_repr},
        )

    def detection_confidence(self, path: str) -> int:
        p = Path(path)
        if p.is_file() and p.name in _FILENAMES:
            return CONFIDENCE_EXACT_FILENAME
        if p.is_dir():
            return CONFIDENCE_DIRECTORY
        return 0


register_plugin({cls}Plugin())
'''


def _render_build(spec: PluginSpec) -> str:
    # Emit ENTRIES from the spec. Each entry is (directive, bad, good, section).
    entry_lines = ""
    for e in spec.entries:
        directive = getattr(e, "directive", e[0] if isinstance(e, (list, tuple)) else "")
        bad = getattr(e, "bad_value", e[1] if isinstance(e, (list, tuple)) and len(e) > 1 else "")
        good = getattr(e, "good_value", e[2] if isinstance(e, (list, tuple)) and len(e) > 2 else "")
        section = getattr(e, "cis_section", e[3] if isinstance(e, (list, tuple)) and len(e) > 3 else "")
        entry_lines += (
            f'    MisconfigEntry({directive!r}, {bad!r}, {good!r}, '
            f'{section!r}, "", _TARGET),\n'
        )
    if not entry_lines:
        entry_lines = "    # Add MisconfigEntry(directive, bad, good, cis_section, '', _TARGET) here.\n"

    return f'''"""
plugins/{spec.target_id}/build_{spec.target_id}.py — GENERATED by `ccss plugin add`.

Defines the ground-truth ENTRIES for {spec.service_name}. Mirrors the Apache /
Nginx build modules; run via `ccss build --target {spec.target_id}`.
"""

from __future__ import annotations

from config_assessment.plugins.apache_httpd.llm_pipeline import MisconfigEntry

_TARGET = "{spec.target_id}"

ENTRIES: list[MisconfigEntry] = [
{entry_lines}]
'''


# ------------------------------------------------------------------ #
# Public API                                                           #
# ------------------------------------------------------------------ #

def scaffold_plugin(spec: PluginSpec, plugins_dir, benchmark_pdf=None) -> Path:
    """Generate plugins/<target_id>/ with the four plugin files.

    Copies the benchmark PDF into the plugin dir when provided. Returns the path
    of the created directory.
    """
    plugins_dir = Path(plugins_dir)
    plugin_dir = plugins_dir / spec.target_id
    plugin_dir.mkdir(parents=True, exist_ok=True)

    (plugin_dir / "parser.py").write_text(_render_parser(spec), encoding="utf-8")
    (plugin_dir / "rules.py").write_text(_render_rules(spec), encoding="utf-8")
    (plugin_dir / "__init__.py").write_text(_render_init(spec), encoding="utf-8")
    (plugin_dir / f"build_{spec.target_id}.py").write_text(_render_build(spec), encoding="utf-8")

    if benchmark_pdf is not None:
        src = Path(benchmark_pdf)
        if src.exists():
            shutil.copy(src, plugin_dir / src.name)

    return plugin_dir
